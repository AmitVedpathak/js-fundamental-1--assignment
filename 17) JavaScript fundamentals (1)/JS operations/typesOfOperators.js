// Operators in JavaScript can be categorized based on their functionality. Here are some common categories along with examples:

// 1) Arithmetic Operators:

// Perform basic mathematical operations.
let a = 5;
let b = 2;
let sum = a + b; // Addition
let difference = a - b; // Subtraction
let product = a * b; // Multiplication
let quotient = a / b; // Division

// 2) Assignment Operators:

// Assign values to variables, often combined with arithmetic operations.
let x = 10;
x += 5; // Equivalent to x = x + 5;

// 3) Comparison Operators:

// Compare two values and return a Boolean result.
let isEqual = (a === b); // Strict equality
let isNotEqual = (a !== b); // Strict inequality

// 4) Logical Operators:

// Combine or modify Boolean values.
let condition1 = true;
let condition2 = false;
let result = (condition1 && condition2); // Logical AND


// 5) Unary Operators:

// Operate on a single operand.
let num = 5;
num++; // Increment by 1

// 6) Ternary Operator:

// A shorthand for an if-else statement.
let age = 20;
let status = (age >= 18) ? 'Adult' : 'Minor';


// 7) String Concatenation Operator:

// Concatenates two strings
let firstName = 'John';
let lastName = 'Doe';
let fullName = firstName + ' ' + lastName;
console.log(fullName)